package com.fintech.model;

/**
 * RecordType enum defining financial transaction types.
 * 
 * - INCOME: Represents incoming financial transactions
 * - EXPENSE: Represents outgoing financial transactions
 */
public enum RecordType {
    /**
     * Represents incoming financial transactions
     */
    INCOME,
    
    /**
     * Represents outgoing financial transactions
     */
    EXPENSE
}
